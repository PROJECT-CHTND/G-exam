/**
 * 関係の型。方向規約(from → to の意味)は RESTRUCTURE_PLAN.md §3.2 に準拠する。
 * この10種以外の型を追加してはならない。
 *
 * - is_a:            from は to の一種(下位 → 上位)
 * - part_of:         from は to の構成要素
 * - solves:          from は to という問題を解決・緩和する(to は問題側であること)
 * - suffers_from:    from は to という問題を抱える
 * - evolves_to:      from は to を改良して生まれた(from=後継、to=前身。系譜の矢印は必ずこの向き)
 * - contrasts_with:  試験で対比されがち(対称関係。1方向1本だけ登録する)
 * - requires:        from は to を前提とする
 * - used_for:        from は to で採用・応用される/〜に使う
 * - pipeline_next:    手順上、from の次に to が来る
 * - proposed:        (person専用) from は to を提案した。from は必ず kind: "person" のカード
 *
 * 【エッジ追加の恒久ルール(Phase 3ミニバッチ再レビューで確定)】
 * エッジは追加前に必ず from→to を規約どおりの日本語一文として読み下し、文として偽になる組は張らない。
 * 迷ったら張らず、候補として報告する(自信を持って断定できる関係のみ登録する)。
 *
 * 【part_ofの誤用に関する追加ルール(Phase 3項目35バッチレビューで確定)】
 * 「重なる・関連する」を part_of と読み違えない。part_of が成立するのは、fromが無ければtoが
 * 定義として成立しない(構造的な包含関係にある)場合のみ。単に関心・フェーズが重なるだけの
 * 関係(例: MLOpsとCRISP-ML(Q)の運用フェーズ)は used_for 等の別の型を検討する。
 */
export type RelationType =
  | "is_a"
  | "part_of"
  | "evolves_to"
  | "solves"
  | "suffers_from"
  | "used_for"
  | "contrasts_with"
  | "requires"
  | "pipeline_next"
  | "proposed";

/** カードの種別。省略時は "concept" として扱う。 */
export type ConceptKind = "concept" | "problem" | "person";
/** カードの完成度。省略時は "draft" として扱う(P2: 2質問が埋まらない用語は無理にcompleteにしない)。 */
export type ConceptStatus = "draft" | "complete";
/** timeline.ts で定義される era の ID(era-01 〜 era-14)。 */
export type EraId = `era-${string}`;
/** pipeline.ts で定義される stage の ID(stage-1 〜 stage-7)。 */
export type StageId = `stage-${string}`;

export type Concept = {
  id: string;
  term: string;
  reading?: string;
  category: ConceptCategory;
  summary: string;
  examHint: string;
  demo?: string;

  /**
   * 省略時 "concept"。problem は cause/consequence、person は業績の読み替えを行う(§3.1)。
   *
   * 【concept / problem の線引き(Phase 3項目1バッチレビューで明文化)】
   * - problem: 困難・限界を指すカード。solves/suffers_from の受け皿(to側)になり得る、
   *   または unresolved(本質的に未解決)であるもの。例: フレーム問題、シンボルグラウンディング
   *   問題、AI効果(いずれもproblem)。
   * - concept: 主張・予測・思考実験・区分を指すカード。技術的な「解決」の対象ではなく、
   *   議論・立場の整理として2質問を読み替える。例: シンギュラリティ、中国語の部屋、
   *   強いAIと弱いAI(いずれもconcept。「困難」ではなく「立場・仮説」を述べるカードのため)。
   */
  kind?: ConceptKind;
  /** syllabus.ts の項目ID(複数可)。Phase 0 監査時点では暫定(provisional)。 */
  syllabus?: string[];
  /**
   * 技術史タイムラインへのアンカー。concept/problem(person以外)の complete カードは
   * timeline/pipeline の少なくとも一方が必須(Phase 3項目1バッチレビューでproblemにも適用を明確化。
   * frame-problem/toy-problem/symbol-grounding-problem等の既存problemカードは元々この方針で運用済み)。
   *
   * 【era アンカーの意味規則(Phase 3バッチ2〜4レビューで明文化)】
   * era は「発表年がその年代に属するか」ではなく、「どの物語の文脈で学ぶのが最も理解しやすいか」
   * (物語的帰属)を示す。ある era の後日談・到達点・実例として語るのが自然な概念は、
   * 年代が多少ずれていてもその era に置いてよい(例: セマンティックWebは2001年提唱だが、
   * era-04のオントロジーの延長として era-04 に置く)。
   * 一方、複数の era にまたがって繰り返し語られる概念のうち、タスクでもプロジェクトでもない
   * もの(例: 特定の技術に紐づかない議論・思考実験)は、無理に1つの era を選ばずアンカー未設定
   * (draft)のままにしてよい。捏造禁止の原則は「存在しない事実を書かない」ことであり、
   * 「物語上の所属先を決められる概念に慎重にアンカーを張ること」を妨げない。
   *
   * 【タスクハブカードのera到達点アンカー規則(Phase 3タスクカードバッチレビューで確定)】
   * 「入力→出力」で定義される課題(例: 機械翻訳、質問応答、音声認識)のハブカードは、
   * 複数eraにまたがっていても、現在の主流手法が属する枝のeraに「到達点」としてアンカーして
   * よい。条件: 本文(bornToSolve/beforeAndGapのいずれか)に、その課題がどのeraで何によって
   * 解かれてきたかという手法の変遷を1行以上含めること(定義のコピーで終わらせない)。
   * 例: 機械翻訳(ルールベース era-02 → 統計的 era-06 → ニューラル era-11)は、現在の主流である
   * ニューラル機械翻訳の文脈(era-11)に到達点としてアンカーする。
   * この規則は「複数eraにまたがる概念はdraft」という一般則の下位規則であり、タスク性を持つ
   * ハブカードに限って優先適用する(前段落の一般則は、タスク性を持たない議論・思考実験などに
   * 適用する)。
   */
  timeline?: EraId;
  /** 社会実装パイプラインへのアンカー。 */
  pipeline?: StageId;
  /**
   * 質問①(concept)/ cause(problem): 何の問題を解決するために生まれたか。
   *
   * 【bornToSolve / beforeAndGap の目標形式(draft昇格キャンペーン第1バッチ較正レビューで確定)】
   * ゲートは「2質問が埋まっているか」という真偽だけでなく、内容の深さを見る。目標形式は
   * 以下の鎖を意識すること:
   *   ①前任(または無対策状態)が抱えていた具体的な問題を名指す
   *   →②本手法がそれをどう解くか
   *   →③本手法にも残る弱点
   *   →④(あれば)その弱点を引き取った後継技術
   * セルフチェック: 手法名を伏せて bornToSolve だけを読み、「何に困っていたか」が具体的に
   * 読み取れるか自問する。「〜を実践するために使う」「〜を区別するために使う」のような、
   * 手法名の目的をただ言い換えただけの文(困っていた具体的な状況が書かれていない)は
   * 書き直し対象。模範例: rmse(①MSEは単位が異なり解釈しにくい →②平方根で単位を戻す
   * →③外れ値への敏感さはMSEと同様に残る、という鎖が本文だけで追える)。
   */
  bornToSolve?: string;
  /** 質問②(concept)/ consequence(problem): その前は何が使われていて、何が足りなかったか。 */
  beforeAndGap?: string;
  /** この用語を含む白紙再現のお題を1つ。 */
  recall?: string;
  /**
   * 省略時 "draft"。2質問・エッジ・背骨アンカーが揃って初めて "complete" にする。
   *
   * 【draft昇格の鎖基準(第1バッチ較正レビューで追加)】
   * 上記の形式条件(2質問・エッジ2本・アンカー)に加え、bornToSolve/beforeAndGapが
   * 前述の「①前任の問題→②解決→③残る弱点→④(あれば)後継」の鎖を実際に満たしているかを
   * 確認すること。鎖を満たせない(=手法名の目的の言い換えで終わっている)カードは、
   * 形式条件を満たしていても complete にしない。1カテゴリで100%昇格になった場合は、
   * この鎖基準を無作為抽出したカードで再確認し、根拠を報告に明記する。
   */
  status?: ConceptStatus;
  /**
   * kind: problem 専用。特定の技術で「解決済み」とは言えず、部分的な緩和や
   * 論争が今も続いている問題であることを示す(例: フレーム問題、トイ・プロブレム)。
   * true の場合、validate の「未解決問題」警告は対象外とし、UIでは
   * 「本質的に未解決」バッジとして明示する(それ自体が頻出論点のため)。
   *
   * 【使い分けの原則(Phase 3ミニバッチ再レビューで確定)】
   * unresolved は「本質的に未解決」(フレーム問題型: 議論・部分的緩和が続くのみで、
   * 決定的な solves エッジを張れる関係にない)専用。「対策カード未整備」(データ未整備型:
   * 教科書的な対策は存在するが、そのカードがまだこのデータセットにない)には使わない。
   * 後者は無理にフラグで隠さず、validate の WARNING に残す(不足の可視化こそが正しい状態)。
   */
  unresolved?: boolean;
  /**
   * unresolved: true の意味分岐(Phase 3掃討バッチA〜Cレビューで確定)。
   *
   * フレーム問題型(open)とノーフリーランチの定理型(proven-limit)では「未解決」の意味が
   * 本質的に異なる。前者は議論・研究が今も続く未決着の問題(将来 solves エッジが張られる
   * 可能性を排除しない)。後者は数学的に証明された恒久的な制約であり、「未解決」ではなく
   * 「原理的に解決不可能であることが証明済み」という逆の確定的事実である。この2つを
   * 同じバッジ文言(「本質的に未解決」)で表示すると、証明済みの定理を指して事実誤りになる。
   *
   * - "open"(省略時のデフォルト): 議論・部分的緩和が続くのみで決着していない。
   *   例: フレーム問題、シンボルグラウンディング問題、AI効果、トイ・プロブレム。
   *   UI表示:「本質的に未解決」
   * - "proven-limit": 数学的に証明された恒久的な制約。技術の進歩によって将来解消される
   *   性質のものではない。例: ノーフリーランチの定理。
   *   UI表示:「原理的制約(証明済み)」
   */
  unresolvedReason?: "open" | "proven-limit";
};

